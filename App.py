import customtkinter # libreria para crear interfaces gráficas personalizadas
import tkinter # libreria para crear interfaces gráficas


# Configuración de la apariencia de la interfaz gráfica
customtkinter.set_appearance_mode("system") # Establece el modo de apariencia de la interfaz gráfica según el sistema operativo (claro u oscuro)
customtkinter.set_default_color_theme("blue") # Establece el tema de color predeterminado para la interfaz gráfica (en este caso, azul)